    private void BtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnExcluirActionPerformed
        // TODO add your handling code here:[
        c = getSelectedObject();
        if(c != null){
            String opcoes[] = {"Sim", "Não"};
            int op = JOptionPane.showOptionDialog(this, 
                    "Deseja remover esta Cidade: " + c.getNome() + "?", 
                    "Confirma a remoção",
                    0, 
                    JOptionPane.QUESTION_MESSAGE, 
                    null,
                    opcoes, 
                    opcoes[1]);
            if(op == 0){
                repository.remover(c);
                atualizaLista();
            }
        }
        
    }//GEN-LAST:event_BtnExcluirActionPerformed

    
    private Cidade getSelectedObject(){        
        Cidade selecionado = null;        
        int linhaSelecionada = GrdCidades.getSelectedRow();        
        if(linhaSelecionada >= 0){
            selecionado = lista.get(linhaSelecionada);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um objeto da tabela!");
        }
        return selecionado;        
    }
    
