    public void atualizaLista() {
        String[] nomesColunas = {"Código", "Nome", "Uf"};
        lista = repository.getAll();
        Object[][] dadosArray = new Object[lista.size()][nomesColunas.length];
        for (int i = 0; i < lista.size(); i++) {
            c = lista.get(i);
            dadosArray[i][0] = c.getCodigo();
            dadosArray[i][1] = c.getNome();
            dadosArray[i][2] = c.getUf();
            DefaultTableModel modelo = new DefaultTableModel(dadosArray, nomesColunas);
            GrdCidades.setModel(modelo);
        }
    }

